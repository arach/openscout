const NOW = 1_700_000_000_000;

function lane(overrides: Partial<AgentLane> & { agent: Agent }): AgentLane {
  return {
    id: overrides.agent.id,
    source: "scout",
    observe: null,
    lastActiveAt: NOW - 5_000,
    current: true,
    ...overrides,
  };
}

describe("lane display labels", () => {
  test("uses project for auto-generated native session names", () => {
    const agent = stubAgent("Grok 0f8eddbb");
    agent.agentClass = "organic";
    agent.harness = "grok";
    agent.project = "openscout";

    expect(isAutoGeneratedLaneAgentName(agent)).toBe(true);
    expect(hasDesignedAgentCard(agent, "native")).toBe(false);
    expect(lanePrimaryLabel(agent, "native")).toBe("openscout");
    expect(laneStatusLabel(agent, "native")).toBe("grok");
    expect(laneContextLabel(agent, "native")).toBe("grok");
  });

  test("keeps deliberate scout agent card names", () => {
    const agent = stubAgent("openscout-card");
    agent.project = "openscout";
    agent.harness = "codex";
    agent.harnessSessionId = "sess-codex-live";

    expect(isAutoGeneratedLaneAgentName(agent)).toBe(false);
    expect(hasDesignedAgentCard(agent, "scout")).toBe(true);
    expect(lanePrimaryLabel(agent, "scout")).toBe("openscout-card");
    expect(laneStatusLabel(agent, "scout")).toBe("codex");
    expect(laneContextLabel(agent, "scout")).toBe("openscout");
  });
});

