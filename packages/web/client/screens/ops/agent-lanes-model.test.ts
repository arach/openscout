describe("sortLanesWithStableOrder", () => {
  test("keeps existing lanes in first-seen order as activity timestamps change", () => {
    const order = createStableLaneOrder();
    const alpha = lane({ agent: stubAgent("alpha") });
    const beta = lane({ agent: stubAgent("beta"), lastActiveAt: NOW - 60_000 });

    const first = sortLanesWithStableOrder([alpha, beta], order);
    expect(first.lanes.map((item) => item.id)).toEqual(["agent:alpha", "agent:beta"]);

    const bumpedBeta = { ...beta, lastActiveAt: NOW + 30_000 };
    const staleAlpha = { ...alpha, lastActiveAt: NOW - 120_000 };
    const second = sortLanesWithStableOrder([bumpedBeta, staleAlpha], order);
    expect(second.lanes.map((item) => item.id)).toEqual(["agent:alpha", "agent:beta"]);
    expect(second.newLaneIds).toEqual([]);
  });

  test("appends newly working agents at the end", () => {
    const order = createStableLaneOrder();
    const alpha = lane({ agent: stubAgent("alpha") });
    sortLanesWithStableOrder([alpha], order);

    const gamma = lane({ agent: stubAgent("gamma") });
    const result = sortLanesWithStableOrder([alpha, gamma], order);
    expect(result.lanes.map((item) => item.id)).toEqual(["agent:alpha", "agent:gamma"]);
    expect(result.newLaneIds).toEqual(["agent:gamma"]);
  });

  test("re-admits returning agents at the end after they leave the roster", () => {
    const order = createStableLaneOrder();
    const alpha = lane({ agent: stubAgent("alpha") });
    sortLanesWithStableOrder([alpha], order);
    sortLanesWithStableOrder([], order);

    const result = sortLanesWithStableOrder([alpha], order);
    expect(result.lanes.map((item) => item.id)).toEqual(["agent:alpha"]);
    expect(result.newLaneIds).toEqual(["agent:alpha"]);
  });
});

