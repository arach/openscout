const NOW = 1_700_000_000_000;

function lane(overrides: Partial<AgentLane> & { agent: Agent }): AgentLane {
  return {
    id: overrides.agent.id,
    source: "scout",
    observe: null,
    lastActiveAt: NOW - 5_000,
    current: true,
    ...overrides,
  };
}

