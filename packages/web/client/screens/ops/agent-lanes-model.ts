export function observeLastSubstantiveActivityAt(
  observe: ObserveData | null | undefined,
): number | null {
  if (!observe) return null;

  const sessionStart = observe.metadata?.session?.sessionStart;
  let latest: number | null = null;

  for (const event of observe.events) {
    if (!isSubstantiveObserveEvent(event)) continue;
    const wallMs = observeEventWallMs(event, sessionStart);
    if (wallMs === null) continue;
    latest = latest === null ? wallMs : Math.max(latest, wallMs);
  }

  if (typeof sessionStart === "number" && Number.isFinite(sessionStart)) {
    for (const file of observe.files) {
      if (!Number.isFinite(file.lastT) || file.lastT < 0) continue;
      const wallMs = sessionStart + file.lastT * 1000;
      latest = latest === null ? wallMs : Math.max(latest, wallMs);
    }
  }

  return latest;
}

function hasLiveSubstantiveWork(observe: ObserveData | null | undefined): boolean {
  if (!observe?.live) return false;
  return observe.events.some(
    (event) => event.live === true && isSubstantiveObserveEvent(event),
  );
}

function hasRecentSubstantiveObserve(
  observe: ObserveData | null | undefined,
  now: number,
  windowMs: number,
): boolean {
  const lastAt = observeLastSubstantiveActivityAt(observe);
  if (lastAt === null) return false;
  return now - lastAt <= windowMs;
}

function laneActivityAt(
  agent: Agent,
  observeEntry: ObserveCacheEntry | undefined,
  now: number,
): number {
  if (hasLiveSubstantiveWork(observeEntry?.data)) {
    return now;
  }

  const observeAt = observeLastSubstantiveActivityAt(observeEntry?.data);
  const agentAt = agent.updatedAt ?? 0;

  if (observeAt !== null) {
    return Math.max(observeAt, agentAt);
  }

  return 0;
}

export function isAgentLaneLive(
  observe: ObserveData | null | undefined,
): boolean {
  return hasLiveSubstantiveWork(observe);
}

const PLACEHOLDER_EVENT_MARKERS = [
  "transcript discovered",
  "waiting for events",
  "no session trace",
  "waiting for a live session",
];

function isWarmupBootObserveEvent(event: ObserveEvent): boolean {
  if (event.kind !== "boot" && event.kind !== "system") return false;
  const text = event.text.trim().toLowerCase();
  const detail = (event.detail ?? "").trim().toLowerCase();
  if (!text.includes("session started")) return false;
  return detail.includes("turns: 0") && detail.includes("status: idle");
}

function isSubstantiveObserveEvent(event: ObserveEvent): boolean {
  if (event.kind === "tool" || event.kind === "think" || event.kind === "ask" || event.kind === "message") {
    return true;
  }
  if (event.kind === "note") return true;
  if (event.kind === "system" || event.kind === "boot") {
    if (isWarmupBootObserveEvent(event)) return false;
    const text = event.text.trim().toLowerCase();
    return !PLACEHOLDER_EVENT_MARKERS.some((marker) => text.includes(marker));
  }
  return false;
}

const ATTRIBUTION_RANK: Record<TailHarness, number> = {
  "scout-managed": 3,
  "hudson-managed": 2,
  unattributed: 1,
};

/** File-discovered transcripts default to unattributed; resolve from live processes. */
export function resolveTranscriptAttribution(
  transcript: TailDiscoveredTranscript,
  processes: TailDiscoveredProcess[],
): TailHarness {
  const cwd = transcript.cwd?.trim();
  if (!cwd) return transcript.harness;

  const matches = processes.filter((proc) => proc.source === transcript.source && proc.cwd === cwd);
  if (matches.length === 0) return transcript.harness;

  return matches.reduce((best, candidate) => {
    const bestRank = ATTRIBUTION_RANK[best.harness] ?? 0;
    const candRank = ATTRIBUTION_RANK[candidate.harness] ?? 0;
    if (candRank > bestRank) return candidate;
    if (candRank === bestRank && candidate.pid < best.pid) return candidate;
    return best;
  }).harness;
}

export function buildLaneFacts(
  transcript: TailDiscoveredTranscript | null | undefined,
  events: TailEvent[],
  agent: Agent,
  processes: TailDiscoveredProcess[],
  observe?: ObserveData | null,
): LaneFacts {
  const session = observe?.metadata?.session;
  const turnContext = latestTurnContext(events);
  const sessionMeta = latestSessionMeta(events);
  const attribution = transcript
    ? resolveTranscriptAttribution(transcript, processes)
    : attributionFromEvents(events);
  const inferredFiles = observe ? filesFromObserveEvents(observe.events) : [];
  const touchedFiles = mergeObserveFiles(observe?.files, inferredFiles);
  const usage = observe?.metadata?.usage ?? usageFromTailEvents(events);

  const facts: LaneFacts = {
    touchedFiles,
    model: stringValue(
      session?.model,
      turnContext?.model,
      nestedRecord(nestedRecord(turnContext, "collaboration_mode"), "settings")?.model,
      agent.model,
    ),
    effort: stringValue(session?.effort, effortFromTurnContext(turnContext)),
    branch: stringValue(
      session?.gitBranch,
      branchFromPayload(turnContext),
      branchFromPayload(sessionMeta),
      agent.branch,
    ),
    cwd: stringValue(session?.cwd, sessionMeta?.cwd, agent.cwd, agent.projectRoot),
    originator: stringValue(session?.originator, sessionMeta?.originator, sessionMeta?.source),
    attribution,
    turn: events.length > 0
      ? turnFactsFromTailEvents(events)
      : (observe?.live ? { phase: "started" } : { phase: "idle" }),
    usage,
    currentTask: currentTaskFromTailEvents(events, observe),
  };

  for (const key of ["model", "effort", "branch", "cwd", "originator", "attribution", "usage", "currentTask"] as const) {
    if (facts[key] === undefined) delete facts[key];
  }
  return facts;
}

/** Work signal for lane admission — aligned with tail "work" display, not lifecycle noise. */
function isLaneAdmissionTailEvent(event: TailEvent): boolean {
  if (isTailNoiseEvent(event)) return false;

  if (event.kind === "tool"
    || event.kind === "tool-result"
    || event.kind === "assistant"
    || event.kind === "user") {
    return true;
  }

  if (event.kind !== "system") return false;

  if (event.source === "grok") {
    return /^turn /u.test(event.summary);
  }

  if (event.source === "codex") {
    const summary = event.summary.trim().toLowerCase();
    if (summary === "task started"
      || summary === "task complete"
      || summary.startsWith("turn aborted")) {
      return true;
    }
    const raw = event.raw;
    if (raw && typeof raw === "object" && !Array.isArray(raw)) {
      const payload = (raw as Record<string, unknown>).payload;
      if (payload && typeof payload === "object" && !Array.isArray(payload)) {
        const payloadType = (payload as Record<string, unknown>).type;
        if (payloadType === "reasoning" && summary !== "[reasoning]") {
          return true;
        }
      }
    }
  }

  return false;
}

function hasRecentLaneAdmissionTailEvents(
  events: TailEvent[],
  now: number,
  windowMs: number,
): boolean {
  return events.some((event) => {
    if (now - event.ts > windowMs) return false;
    return isLaneAdmissionTailEvent(event);
  });
}

function hasDisplayableLaneTrace(observe: ObserveData | null | undefined): boolean {
  return observe?.events.some(isSubstantiveObserveEvent) ?? false;
}

function normalizeTailSessionRef(value: string | null | undefined): string | null {
  const trimmed = value?.trim();
  if (!trimmed) return null;
  const leaf = trimmed.split("/").pop() ?? trimmed;
  return leaf.endsWith(".jsonl") ? leaf.slice(0, -".jsonl".length) : leaf;
}

function tailSessionRefsForTranscript(
  transcript: TailDiscoveredTranscript,
): string[] {
  const refs = new Set<string>();
  for (const candidate of [
    transcript.sessionId,
    transcript.transcriptPath,
    normalizeTailSessionRef(transcript.sessionId),
    normalizeTailSessionRef(transcript.transcriptPath),
  ]) {
    const trimmed = candidate?.trim();
    if (trimmed) refs.add(trimmed);
  }
  return [...refs];
}

function tailEventsForTranscript(
  transcript: TailDiscoveredTranscript,
  eventsBySession: Map<string, TailEvent[]>,
): TailEvent[] {
  const refs = tailSessionRefsForTranscript(transcript);
  const seen = new Set<string>();
  const events: TailEvent[] = [];
  for (const ref of refs) {
    for (const event of eventsBySession.get(ref) ?? []) {
      if (seen.has(event.id)) continue;
      seen.add(event.id);
      events.push(event);
    }
  }
  return events;
}

function scoutSessionRefsForAgent(agent: Agent): string[] {
  const refs = new Set<string>();
  const sessionId = agent.harnessSessionId?.trim();
  if (sessionId) {
    refs.add(sessionId);
    const normalized = normalizeTailSessionRef(sessionId);
    if (normalized) refs.add(normalized);
    const uuid = extractSessionUuid(sessionId);
    if (uuid) refs.add(uuid);
  }
  for (const candidate of [
    agent.harnessLogPath,
    agent.conversationId,
    extractSessionUuid(agent.harnessLogPath),
    extractSessionUuid(agent.conversationId),
  ]) {
    const trimmed = candidate?.trim();
    if (trimmed) refs.add(trimmed);
  }
  return [...refs];
}

/** Prefer deliberate scout cards over relay placeholders when several agents share a session ref. */
function scoutBindingRank(agent: Agent): number {
  if (!hasDesignedAgentCard(agent, "scout")) return 0;
  if (hasProviderHarnessSession(agent)) return 2;
  return 1;
}

function bindScoutAgent(
  scoutBySession: Map<string, Agent>,
  ref: string,
  agent: Agent,
): void {
  const existing = scoutBySession.get(ref);
  if (!existing) {
    scoutBySession.set(ref, agent);
    return;
  }
  const existingRank = scoutBindingRank(existing);
  const nextRank = scoutBindingRank(agent);
  if (nextRank > existingRank) {
    scoutBySession.set(ref, agent);
    return;
  }
  if (nextRank === existingRank && agent.id.localeCompare(existing.id) < 0) {
    scoutBySession.set(ref, agent);
  }
}

function scoutAgentForTranscript(
  transcript: TailDiscoveredTranscript,
  scoutBySession: Map<string, Agent>,
): Agent | undefined {
  for (const ref of tailSessionRefsForTranscript(transcript)) {
    const agent = scoutBySession.get(ref);
    if (agent) return agent;
  }
  return undefined;
}

function agentSharesTranscriptSession(
  agent: Agent,
  representedSessionRefs: ReadonlySet<string>,
): boolean {
  return scoutSessionRefsForAgent(agent).some((ref) => representedSessionRefs.has(ref));
}

function sessionSubstantiveLastActiveAt(
  sessionId: string | null | undefined,
  eventsBySession: Map<string, TailEvent[]>,
  transcript?: TailDiscoveredTranscript,
): number {
  const events = transcript
    ? tailEventsForTranscript(transcript, eventsBySession)
    : (sessionId?.trim() ? eventsBySession.get(sessionId.trim()) ?? [] : []);
  let latest = 0;
  for (const event of events) {
    if (!isLaneAdmissionTailEvent(event)) continue;
    latest = Math.max(latest, event.ts);
  }
  return latest;
}

function hasNativeLaneWorkingSignal(
  events: TailEvent[],
  now: number,
  windowMs: number,
): boolean {
  return hasRecentLaneAdmissionTailEvents(events, now, windowMs);
}

/** True when the lane represents an agent actively doing work within the horizon. */
export function isAgentLaneWorking(
  lane: AgentLane,
  now: number = Date.now(),
  windowMs: number = agentLaneHorizonWindowMs(DEFAULT_AGENT_LANE_HORIZON),
  sessionSubstantiveAt = 0,
): boolean {
  const { agent, observe, lastActiveAt, source } = lane;
  if (lastActiveAt <= 0 || now - lastActiveAt > windowMs) {
    return false;
  }

  if (source === "native") {
    if (sessionSubstantiveAt > 0 && now - sessionSubstantiveAt <= windowMs) {
      return true;
    }
    return hasLiveSubstantiveWork(observe)
      || hasRecentSubstantiveObserve(observe, now, windowMs);
  }

  if (sessionSubstantiveAt > 0 && now - sessionSubstantiveAt <= windowMs) {
    return true;
  }

  if (hasLiveSubstantiveWork(observe)) {
    return true;
  }

  if (hasRecentSubstantiveObserve(observe, now, windowMs)) {
    return true;
  }

  return false;
}

function compareLanesById(a: AgentLane, b: AgentLane): number {
  return a.id.localeCompare(b.id);
}

const SESSION_UUID_RE = /[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}/i;

function extractSessionUuid(value: string | null | undefined): string | null {
  const match = value?.match(SESSION_UUID_RE);
  return match?.[0]?.toLowerCase() ?? null;
}

export type StableLaneOrder = {
  slots: Map<string, number>;
  nextSlot: number;
};

export function createStableLaneOrder(): StableLaneOrder {
  return { slots: new Map(), nextSlot: 0 };
}

/** Keep lane columns in first-seen order; new lanes append at the end. */
export function sortLanesWithStableOrder(
  lanes: AgentLane[],
  order: StableLaneOrder,
): { lanes: AgentLane[]; newLaneIds: string[] } {
  const newLaneIds: string[] = [];

  for (const lane of lanes) {
    const slotKey = lane.id;
    if (!order.slots.has(slotKey)) {
      order.slots.set(slotKey, order.nextSlot);
      order.nextSlot += 1;
      newLaneIds.push(lane.id);
    }
  }

  const activeSlotKeys = new Set(lanes.map((lane) => lane.id));
  for (const slotKey of [...order.slots.keys()]) {
    if (!activeSlotKeys.has(slotKey)) order.slots.delete(slotKey);
  }

  const sorted = [...lanes].sort((left, right) => {
    const slotLeft = order.slots.get(left.id) ?? Number.MAX_SAFE_INTEGER;
    const slotRight = order.slots.get(right.id) ?? Number.MAX_SAFE_INTEGER;
    if (slotLeft !== slotRight) return slotLeft - slotRight;
    return compareLanesById(left, right);
  });

  return { lanes: sorted, newLaneIds };
}

function canonicalScoutSessionKey(agent: Agent): string | null {
  return extractSessionUuid(agent.harnessSessionId)
    ?? normalizeTailSessionRef(agent.harnessSessionId)
    ?? agent.harnessSessionId?.trim()
    ?? null;
}

function agentLabel(agent: Agent): string {
  return agent.name.trim() || agent.id;
}

function rosterIssueId(kind: AgentLaneRosterIssueKind, sessionRef: string): string {
  return `${kind}:${sessionRef}`;
}

function pushRosterIssue(
  issues: AgentLaneRosterIssue[],
  seen: Set<string>,
  issue: Omit<AgentLaneRosterIssue, "id"> & { id?: string },
): void {
  const sessionRef = issue.sessionRef ?? issue.kind;
  const id = issue.id ?? rosterIssueId(issue.kind, sessionRef);
  if (seen.has(id)) return;
  seen.add(id);
  issues.push({ ...issue, id });
}

function selectedScoutAgentForSession(
  agents: Agent[],
  scoutBySession: Map<string, Agent>,
): Agent | undefined {
  for (const agent of agents) {
    for (const ref of scoutSessionRefsForAgent(agent)) {
      const bound = scoutBySession.get(ref);
      if (bound && agents.some((candidate) => candidate.id === bound.id)) {
        return bound;
      }
    }
  }
  return undefined;
}

/** Cursor process-monitor logs rotate files under one app sessionId. */
function transcriptGroupingKey(transcript: TailDiscoveredTranscript): string {
  if (transcript.source === "cursor") {
    return `${transcript.source}:path:${transcript.transcriptPath}`;
  }
  const sessionId = transcript.sessionId?.trim();
  return sessionId
    ? `${transcript.source}:${sessionId}`
    : `${transcript.source}:path:${transcript.transcriptPath}`;
}

function dedupeCursorTranscriptsForLanes(
  transcripts: TailDiscoveredTranscript[],
): TailDiscoveredTranscript[] {
  const bestBySession = new Map<string, TailDiscoveredTranscript>();
  const others: TailDiscoveredTranscript[] = [];
  for (const transcript of transcripts) {
    if (transcript.source !== "cursor") {
      others.push(transcript);
      continue;
    }
    const sessionId = transcript.sessionId?.trim() || transcript.transcriptPath;
    const existing = bestBySession.get(sessionId);
    if (!existing || transcript.mtimeMs > existing.mtimeMs) {
      bestBySession.set(sessionId, transcript);
    }
  }
  return [...others, ...bestBySession.values()];
}

const PROVIDER_HARNESS_TRANSPORTS = new Set([
  "codex_app_server",
  "claude_stream_json",
  "pairing_bridge",
]);

/** Configured registry placeholders without transport — not part of live lane roster diagnostics. */
export function isLaneRosterFleetAgent(agent: Agent): boolean {
  if (agent.retiredFromFleet || agent.staleLocalRegistration) return false;
  if (isTransportRefInHarnessSlot(agent.harnessSessionId, agent.terminalSurface)) return true;
  if (agent.terminalSurface || agent.transport?.trim()) return true;
  if (agent.state === "in_flight") return true;
  return hasProviderHarnessSession(agent);
}

/** Active provider transports should carry a harness session id; roster cards and terminal relays do not. */
export function expectsProviderHarnessSession(agent: Agent): boolean {
  if (agent.agentClass === "organic") return false;
  if (agent.terminalSurface) return false;
  const transport = agent.transport?.trim();
  if (!transport || transport === "tmux" || transport === "zellij") return false;
  if (!agent.harness?.trim()) return false;
  if (agent.state !== "in_flight") return false;
  return PROVIDER_HARNESS_TRANSPORTS.has(transport);
}

function collectDuplicateTranscriptIssues(
  transcripts: TailDiscoveredTranscript[],
): AgentLaneRosterIssue[] {
  const issues: AgentLaneRosterIssue[] = [];
  const seen = new Set<string>();
  const bySession = new Map<string, TailDiscoveredTranscript[]>();

  for (const transcript of transcripts) {
    const key = transcriptGroupingKey(transcript);
    const bucket = bySession.get(key) ?? [];
    bucket.push(transcript);
    bySession.set(key, bucket);
  }

  for (const [sessionRef, bucket] of bySession) {
    if (bucket.length <= 1) continue;
    pushRosterIssue(issues, seen, {
      kind: "duplicate_transcript_session",
      sessionRef,
      transcriptPaths: bucket.map((transcript) => transcript.transcriptPath),
      message: `Tail discovery returned ${bucket.length} transcripts for ${sessionRef}.`,
    });
  }

  return issues;
}

function collectScoutFleetIssues(
  scoutAgents: Agent[],
  scoutBySession: Map<string, Agent>,
): AgentLaneRosterIssue[] {
  const fleetAgents = scoutAgents.filter(isLaneRosterFleetAgent);
  const issues: AgentLaneRosterIssue[] = [];
  const seen = new Set<string>();
  const byCanonical = new Map<string, Agent[]>();

  for (const agent of fleetAgents) {
    const key = canonicalScoutSessionKey(agent);
    if (!key) continue;
    const bucket = byCanonical.get(key) ?? [];
    if (!bucket.some((candidate) => candidate.id === agent.id)) {
      bucket.push(agent);
    }
    byCanonical.set(key, bucket);
  }

  const pollutedHarness = fleetAgents.filter(
    (agent) => isTransportRefInHarnessSlot(agent.harnessSessionId, agent.terminalSurface),
  );
  if (pollutedHarness.length > 0) {
    pushRosterIssue(issues, seen, {
      kind: "harness_session_polluted",
      sessionRef: "harness-slot",
      agentIds: pollutedHarness.map((agent) => agent.id),
      agentNames: pollutedHarness.map(agentLabel),
      message: `${pollutedHarness.length} agent(s) store a transport ref in harnessSessionId — use terminalSurface for attach and harnessSessionId for provider ids only.`,
    });
  }

  const unboundHarness = fleetAgents.filter(
    (agent) => expectsProviderHarnessSession(agent) && !hasProviderHarnessSession(agent),
  );
  if (unboundHarness.length > 0) {
    pushRosterIssue(issues, seen, {
      kind: "harness_session_unbound",
      sessionRef: "fleet",
      agentIds: unboundHarness.map((agent) => agent.id),
      agentNames: unboundHarness.map(agentLabel),
      message: `${unboundHarness.length} scout agent(s) have no provider harness session bound (lanes use tail transcripts, not agent.id).`,
    });
  }

  for (const [sessionRef, agents] of byCanonical) {
    if (agents.length <= 1) continue;

    const selected = selectedScoutAgentForSession(agents, scoutBySession);
    const shadowed = agents.filter((agent) => agent.id !== selected?.id);
    if (shadowed.length === 0) continue;
    pushRosterIssue(issues, seen, {
      kind: "session_binding_conflict",
      sessionRef,
      agentIds: agents.map((agent) => agent.id),
      agentNames: agents.map(agentLabel),
      selectedAgentId: selected?.id,
      message: selected
        ? `Multiple scout agents claim ${sessionRef}; showing ${agentLabel(selected)}.`
        : `Multiple scout agents claim ${sessionRef} but none could be selected.`,
    });
  }

  return issues;
}

export function rosterIssuesFromTailDiscovery(
  discovery: { issues?: Array<{
    kind: "transcript_path_collision";
    sessionKey: string;
    message: string;
    transcriptPaths: string[];
  }> } | null | undefined,
): AgentLaneRosterIssue[] {
  if (!discovery?.issues?.length) return [];
  const seen = new Set<string>();
  const issues: AgentLaneRosterIssue[] = [];
  for (const issue of discovery.issues) {
    // Cursor process-monitor logs rotate files under one app session id; not a harness collision.
    if (issue.sessionKey.startsWith("cursor:")) continue;
    if (seen.has(issue.sessionKey)) continue;
    seen.add(issue.sessionKey);
    issues.push({
      id: rosterIssueId("transcript_path_collision", issue.sessionKey),
      kind: "transcript_path_collision",
      sessionRef: issue.sessionKey,
      transcriptPaths: issue.transcriptPaths,
      message: issue.message,
    });
  }
  return issues;
}

export function buildAgentLanes(input: {
  transcripts: TailDiscoveredTranscript[];
  tailEvents: TailEvent[];
  processes?: TailDiscoveredProcess[];
  scoutAgents?: Agent[];
  observeCache?: Record<string, ObserveCacheEntry | undefined>;
  now: number;
  workingOnly?: boolean;
  horizon?: AgentLaneHorizonKey;
  /** @deprecated Pass scoutAgents instead. */
  agents?: Agent[];
}): AgentLaneRosterBuild {
  const {
    transcripts: rawTranscripts,
    tailEvents,
    processes = [],
    scoutAgents = input.agents ?? [],
    observeCache = {},
    now,
    workingOnly = true,
    horizon = DEFAULT_AGENT_LANE_HORIZON,
  } = input;
  const transcripts = dedupeCursorTranscriptsForLanes(rawTranscripts);
  const windowMs = agentLaneHorizonWindowMs(horizon);
  const lanes: AgentLane[] = [];

  const eventsBySession = new Map<string, TailEvent[]>();
  for (const event of tailEvents) {
    const bucket = eventsBySession.get(event.sessionId) ?? [];
    bucket.push(event);
    eventsBySession.set(event.sessionId, bucket);
  }

  const scoutBySession = new Map<string, Agent>();
  for (const agent of scoutAgents) {
    for (const ref of scoutSessionRefsForAgent(agent)) {
      bindScoutAgent(scoutBySession, ref, agent);
    }
  }

  const representedTranscriptSessionRefs = new Set<string>();
  const transcriptBoundAgentIds = new Set<string>();
  const rosterIssues: AgentLaneRosterIssue[] = [
    ...collectDuplicateTranscriptIssues(transcripts),
    ...collectScoutFleetIssues(scoutAgents, scoutBySession),
  ];
  const rosterIssueIds = new Set(rosterIssues.map((issue) => issue.id));

  for (const transcript of transcripts) {
    const sessionId = transcript.sessionId?.trim();
    const events = tailEventsForTranscript(transcript, eventsBySession);
    const lastActiveAt = sessionSubstantiveLastActiveAt(sessionId, eventsBySession, transcript);
    const current = lastActiveAt > 0 && now - lastActiveAt <= windowMs;
    if (!current || !hasNativeLaneWorkingSignal(events, now, windowMs)) {
      continue;
    }

    const scoutAgent = scoutAgentForTranscript(transcript, scoutBySession);
    if (scoutAgent) transcriptBoundAgentIds.add(scoutAgent.id);
    const observe = observeDataFromTail(transcript, events, current, { now, windowMs });
    const facts = buildLaneFacts(transcript, events, scoutAgent ?? nativeSessionAgent(transcript, lastActiveAt, current), processes, observe);
    const sessionSubstantiveAt = lastActiveAt;
    const lane: AgentLane = scoutAgent
      ? {
          id: scoutAgent.id,
          agent: { ...scoutAgent, updatedAt: lastActiveAt },
          source: "scout",
          observe,
          facts,
          lastActiveAt,
          current,
        }
      : {
          id: nativeSessionId(transcript),
          agent: nativeSessionAgent(transcript, lastActiveAt, current),
          source: "native",
          observe,
          facts,
          lastActiveAt,
          current,
        };
    if (workingOnly && !isAgentLaneWorking(lane, now, windowMs, sessionSubstantiveAt)) {
      continue;
    }
    if (!hasDisplayableLaneTrace(observe) && !isAgentLaneLive(observe)) {
      continue;
    }
    for (const ref of tailSessionRefsForTranscript(transcript)) {
      representedTranscriptSessionRefs.add(ref);
    }
    lanes.push(lane);
  }

  for (const agent of scoutAgents) {
    if (isIdleCodexRelay(agent)) continue;
    if (!hasDesignedAgentCard(agent, "scout")) continue;
    if (!hasProviderHarnessSession(agent)) continue;
    if (agentSharesTranscriptSession(agent, representedTranscriptSessionRefs)) {
      if (!transcriptBoundAgentIds.has(agent.id)) {
        const sessionRef = canonicalScoutSessionKey(agent) ?? agent.id;
        pushRosterIssue(rosterIssues, rosterIssueIds, {
          kind: "shadowed_scout_card",
          sessionRef,
          agentIds: [agent.id],
          agentNames: [agentLabel(agent)],
          message: `${agentLabel(agent)} is active but hidden because another lane already represents ${sessionRef}.`,
        });
      }
      continue;
    }

    const observeEntry = observeCache[agent.id];
    const sessionSubstantiveAt = sessionSubstantiveLastActiveAt(
      agent.harnessSessionId,
      eventsBySession,
    );
    const lastActiveAt = Math.max(laneActivityAt(agent, observeEntry, now), sessionSubstantiveAt);
    const current = lastActiveAt > 0 && now - lastActiveAt <= windowMs;
    const observe = filterObserveDataForHorizon(observeEntry?.data ?? null, now, windowMs);
    const lane: AgentLane = {
      id: agent.id,
      agent,
      source: "scout",
      observe,
      facts: buildLaneFacts(null, [], agent, processes, observe),
      lastActiveAt,
      current,
    };
    if (workingOnly && !isAgentLaneWorking(lane, now, windowMs, sessionSubstantiveAt)) {
      continue;
    }
    if (!hasDisplayableLaneTrace(observe) && !isAgentLaneLive(observe)) {
      continue;
    }
    lanes.push(lane);
  }

  return {
    lanes: lanes.sort(compareLanesById),
    issues: rosterIssues,
  };
}
