function titleCaseHarness(value: string): string {
  return value ? `${value[0]?.toUpperCase()}${value.slice(1).toLowerCase()}` : "";
}

/** True for tail-synthesized names like "Grok 0f8eddbb" or "Codex 019edd5a". */
export function isAutoGeneratedLaneAgentName(agent: Agent): boolean {
  const name = agent.name.trim();
  if (!name) return true;
  if (agent.agentClass === "organic") return true;

  const harness = agent.harness?.trim();
  if (!harness) return false;

  const head = titleCaseHarness(harness);
  return new RegExp(`^${head}\\s+[0-9a-f]{6}`, "i").test(name);
}

/** Legacy tmux/runtime refs wrongly stored in the harness session slot. */
export function isTransportRefInHarnessSlot(
  harnessSessionId: string | null | undefined,
  terminalSurface: Agent["terminalSurface"] = null,
): boolean {
  const trimmed = harnessSessionId?.trim();
  if (!trimmed) return false;
  if (/^relay[-:]/i.test(trimmed)) return true;
  const surfaceName = terminalSurface?.sessionName?.trim();
  return Boolean(surfaceName && trimmed === surfaceName);
}

/** Provider conversation id present and not polluted by a transport ref. */
export function hasProviderHarnessSession(agent: Agent): boolean {
  const sessionId = agent.harnessSessionId?.trim();
  if (!sessionId) return false;
  return !isTransportRefInHarnessSlot(sessionId, agent.terminalSurface);
}

/** Warmed-up codex app-server relay waiting for work — not an active lane. */
export function isIdleCodexRelay(agent: Agent): boolean {
  return agent.transport === "codex_app_server" && agent.state === "available";
}

/** Poll observe only for agents that may produce lane-worthy trace activity. */
export function shouldPollAgentForLaneObserve(
  agent: Agent,
  now: number = Date.now(),
  horizon: AgentLaneHorizonKey = DEFAULT_AGENT_LANE_HORIZON,
): boolean {
  if (isIdleCodexRelay(agent)) return false;
  if (agent.harnessSessionId?.trim()) return true;
  if (agent.state && /^(working|active|running|in_turn|in_flight|queued|waking|dispatching)$/i.test(agent.state.trim())) {
    return true;
  }
  const windowMs = agentLaneHorizonWindowMs(horizon);
  return agent.updatedAt ? now - agent.updatedAt <= windowMs : false;
}

/** @deprecated Use isTransportRefInHarnessSlot */
