function parseMaybeJsonRecord(value: string | undefined): Record<string, unknown> | null {
  if (!value?.trim()) return null;
  try {
    return metadataRecord(JSON.parse(value));
  } catch {
    return null;
  }
}

function codexToolPayloadName(event: TailEvent): string | undefined {
  const payload = tailPayload(event);
  return stringValue(payload?.name, payload?.type);
}

function codexToolRawArgument(event: TailEvent): string | undefined {
  const payload = tailPayload(event);
  const args = payload?.arguments;
  if (typeof args === "string") return args;
  const input = payload?.input;
  if (typeof input === "string") return input;
  const command = payload?.command;
  if (Array.isArray(command)) {
    return command.filter((part) => typeof part === "string").join(" ");
  }
  if (typeof command === "string") return command;
  return undefined;
}

function codexPatchTextFromTailEvent(event: TailEvent): string | undefined {
  if (event.source !== "codex") return undefined;
  const name = codexToolPayloadName(event)?.toLowerCase();
  if (name !== "apply_patch" && name !== "patch_apply") return undefined;

  const rawArg = codexToolRawArgument(event);
  const parsed = parseMaybeJsonRecord(rawArg);
  return stringValue(parsed?.patch, parsed?.input, rawArg);
}

function diffPreviewFromPatch(patch: string | undefined): ObserveEvent["diff"] | undefined {
  if (!patch?.trim()) return undefined;
  let add = 0;
  let del = 0;
  const previewLines: string[] = [];
  for (const line of patch.split(/\r?\n/)) {
    if (line.startsWith("+") && !line.startsWith("+++")) add += 1;
    if (line.startsWith("-") && !line.startsWith("---")) del += 1;
    if (previewLines.length < 24 && /^(?:\*\*\* |@@|\+\+\+|---|[+-])/.test(line)) {
      previewLines.push(line);
    }
  }
  if (add === 0 && del === 0 && previewLines.length === 0) return undefined;
  return { add, del, preview: previewLines.join("\n") };
}

function toolDetailFromTailEvent(event: TailEvent): string | undefined {
  return codexPatchTextFromTailEvent(event);
}

function tailEventsForHorizon(
  events: TailEvent[],
  now: number,
  windowMs: number | undefined,
): TailEvent[] {
  const sorted = [...events].sort((left, right) => left.ts - right.ts);
  const inWindow = typeof windowMs === "number" && windowMs > 0
    ? sorted.filter((event) => event.ts >= now - windowMs)
    : sorted;
  const displayed = filterTailEventsForDisplay(inWindow, "work");
  return displayed.length > MAX_LANE_TRACE_EVENTS
    ? displayed.slice(-MAX_LANE_TRACE_EVENTS)
    : displayed;
}

/** Build lane observe data from the tail firehose (wall-clock timestamps). */
export function observeDataFromTail(
  transcript: TailDiscoveredTranscript,
  events: TailEvent[],
  current: boolean,
  options?: { now?: number; windowMs?: number },
): ObserveData {
  const now = options?.now ?? Date.now();
  const tail = tailEventsForHorizon(events, now, options?.windowMs);
  const sessionStart = tail[0]?.ts ?? transcript.mtimeMs;

  const observeEvents = tail.map((event): ObserveEvent => {
    const toolFields = observeToolFieldsFromTailEvent(event);
    const detail = tailObserveEventDetail(event, transcript) ?? toolDetailFromTailEvent(event);
    const diff = diffPreviewFromPatch(detail);
    return {
      id: event.id,
      t: Math.max(0, Math.round((event.ts - sessionStart) / 1000)),
      at: event.ts,
      kind: observeKindFromTailEvent(event),
      text: observeTextFromTailEvent(event, toolFields),
      tool: toolFields.tool,
      arg: toolFields.arg,
      result: toolFields.result,
      diff,
      detail,
      live: current && event.id === tail[tail.length - 1]?.id,
    };
  });

  const placeholderEvents: ObserveEvent[] = observeEvents.length > 0
    ? observeEvents
    : (Date.now() - transcript.mtimeMs <= NATIVE_DISCOVERED_FRESH_MS
        ? [{
            id: `${nativeSessionId(transcript)}:discovered`,
            t: 0,
            kind: "system",
            text: `Native ${transcript.source} transcript discovered.`,
            detail: transcript.cwd ?? transcript.transcriptPath,
        }]
        : []);
  const files = filesFromObserveEvents(observeEvents);
  const usage = usageFromTailEvents(events);

  return {
    events: placeholderEvents,
    files,
    live: current && observeEvents.length > 0,
    metadata: {
      session: observeSessionFromTail(transcript, events, sessionStart),
      usage,
    },
  };
}

