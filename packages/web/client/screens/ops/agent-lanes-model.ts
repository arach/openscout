function stableHash(value: string): string {
  let hash = 2166136261;
  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return (hash >>> 0).toString(36);
}

export function nativeSessionId(transcript: TailDiscoveredTranscript): string {
  const sessionId = transcript.sessionId?.trim();
  if (sessionId) {
    return `native:${transcript.source}:${sessionId}`;
  }
  return `native:${transcript.source}:path:${stableHash(transcript.transcriptPath)}`;
}

function metadataRecord(value: unknown): Record<string, unknown> | null {
  return value && typeof value === "object" && !Array.isArray(value)
    ? value as Record<string, unknown>
    : null;
}

function tailPayload(event: TailEvent): Record<string, unknown> | null {
  const raw = metadataRecord(event.raw);
  return metadataRecord(raw?.payload);
}

function rawRecordType(event: TailEvent): string | null {
  const raw = metadataRecord(event.raw);
  const type = raw?.type;
  return typeof type === "string" ? type : null;
}

function payloadType(event: TailEvent): string | null {
  const payload = tailPayload(event);
  const type = payload?.type;
  return typeof type === "string" ? type : null;
}

function stringValue(...values: unknown[]): string | undefined {
  for (const value of values) {
    if (typeof value !== "string") continue;
    const trimmed = value.trim();
    if (trimmed) return trimmed;
  }
  return undefined;
}

function numberValue(value: unknown): number | undefined {
  return typeof value === "number" && Number.isFinite(value) ? value : undefined;
}

function nestedRecord(
  record: Record<string, unknown> | null | undefined,
  key: string,
): Record<string, unknown> | null {
  return metadataRecord(record?.[key]);
}

function latestPayloadByEntryType(
  events: TailEvent[],
  entryType: string,
): Record<string, unknown> | null {
  let latest: { event: TailEvent; payload: Record<string, unknown> } | null = null;
  for (const event of events) {
    if (rawRecordType(event) !== entryType) continue;
    const payload = tailPayload(event);
    if (!payload) continue;
    if (!latest || event.ts >= latest.event.ts) latest = { event, payload };
  }
  return latest?.payload ?? null;
}

function latestTurnContext(events: TailEvent[]): Record<string, unknown> | null {
  return latestPayloadByEntryType(events, "turn_context");
}

function latestSessionMeta(events: TailEvent[]): Record<string, unknown> | null {
  return latestPayloadByEntryType(events, "session_meta");
}

function effortFromTurnContext(turnContext: Record<string, unknown> | null): string | undefined {
  const collaboration = nestedRecord(turnContext, "collaboration_mode");
  const settings = nestedRecord(collaboration, "settings");
  return stringValue(turnContext?.effort, settings?.reasoning_effort);
}

function branchFromPayload(payload: Record<string, unknown> | null): string | undefined {
  return stringValue(
    payload?.gitBranch,
    payload?.git_branch,
    payload?.currentBranch,
    payload?.current_branch,
    payload?.branch,
  );
}

function usageFromCodexTokenEvent(event: TailEvent): ObserveUsageMeta | null {
  if (event.source !== "codex") return null;
  if (rawRecordType(event) !== "event_msg" || payloadType(event) !== "token_count") {
    const summaryTotal = event.summary.match(/^tokens? · ([0-9,]+)/i)?.[1]?.replace(/,/g, "");
    const totalTokens = summaryTotal ? Number.parseInt(summaryTotal, 10) : NaN;
    return Number.isFinite(totalTokens) ? { totalTokens } : null;
  }

  const payload = tailPayload(event);
  const info = nestedRecord(payload, "info");
  const total = nestedRecord(info, "total_token_usage");
  const rateLimits = nestedRecord(payload, "rate_limits");
  if (!total && !info && !rateLimits) return null;

  const usage: ObserveUsageMeta = {};
  const inputTokens = numberValue(total?.input_tokens);
  const outputTokens = numberValue(total?.output_tokens);
  const reasoningOutputTokens = numberValue(total?.reasoning_output_tokens);
  const cacheReadInputTokens = numberValue(total?.cached_input_tokens);
  const cacheCreationInputTokens = numberValue(total?.cache_creation_input_tokens);
  const totalTokens = numberValue(total?.total_tokens);
  const contextWindowTokens = numberValue(info?.model_context_window);
  const serviceTier = stringValue(payload?.service_tier, rateLimits?.service_tier);
  const speed = stringValue(payload?.speed, rateLimits?.speed);
  const planType = stringValue(payload?.plan_type, rateLimits?.plan_type);

  if (inputTokens !== undefined) usage.inputTokens = inputTokens;
  if (outputTokens !== undefined) usage.outputTokens = outputTokens;
  if (reasoningOutputTokens !== undefined) usage.reasoningOutputTokens = reasoningOutputTokens;
  if (cacheReadInputTokens !== undefined) usage.cacheReadInputTokens = cacheReadInputTokens;
  if (cacheCreationInputTokens !== undefined) usage.cacheCreationInputTokens = cacheCreationInputTokens;
  if (totalTokens !== undefined) usage.totalTokens = totalTokens;
  if (contextWindowTokens !== undefined) usage.contextWindowTokens = contextWindowTokens;
  if (serviceTier) usage.serviceTier = serviceTier;
  if (speed) usage.speed = speed;
  if (planType) usage.planType = planType;
  return Object.keys(usage).length > 0 ? usage : null;
}

function usageFromTailEvents(events: TailEvent[]): ObserveUsageMeta | undefined {
  let latest: { ts: number; usage: ObserveUsageMeta } | null = null;
  for (const event of events) {
    if (event.source !== "codex") continue;
    if (payloadType(event) !== "token_count" && !/^tokens?(?: ·|$)/i.test(event.summary.trim())) {
      continue;
    }
    const usage = usageFromCodexTokenEvent(event);
    if (!usage) continue;
    if (!latest || event.ts >= latest.ts) latest = { ts: event.ts, usage };
  }
  return latest?.usage;
}

function codexSessionMetaFromTailEvents(
  transcript: TailDiscoveredTranscript,
  events: TailEvent[],
): ObserveSessionMeta {
  const sessionMeta = latestSessionMeta(events);
  const turnContext = latestTurnContext(events);
  const collaboration = nestedRecord(turnContext, "collaboration_mode");
  const settings = nestedRecord(collaboration, "settings");
  const sandboxPolicy = metadataRecord(turnContext?.sandbox_policy);
  const permissionProfile = metadataRecord(turnContext?.permission_profile);

  const session: ObserveSessionMeta = {
    adapterType: transcript.source,
    cwd: stringValue(turnContext?.cwd, sessionMeta?.cwd, transcript.cwd) ?? undefined,
    externalSessionId: stringValue(sessionMeta?.id, transcript.sessionId) ?? undefined,
    threadId: stringValue(turnContext?.turn_id) ?? undefined,
    threadPath: transcript.transcriptPath,
    model: stringValue(turnContext?.model, settings?.model) ?? undefined,
    effort: effortFromTurnContext(turnContext),
    gitBranch: branchFromPayload(turnContext) ?? branchFromPayload(sessionMeta),
    cliVersion: stringValue(sessionMeta?.cli_version),
    entrypoint: stringValue(sessionMeta?.thread_source, sessionMeta?.source),
    originator: stringValue(sessionMeta?.originator, sessionMeta?.source),
    source: stringValue(sessionMeta?.source),
    approvalPolicy: stringValue(turnContext?.approval_policy),
    sandbox: stringValue(sandboxPolicy?.type, turnContext?.sandbox),
    permissionMode: stringValue(permissionProfile?.type, turnContext?.permission_mode),
    userType: stringValue(sessionMeta?.user_type, turnContext?.user_type),
    modelProvider: stringValue(sessionMeta?.model_provider, turnContext?.model_provider),
    timezone: stringValue(turnContext?.timezone),
  };

  for (const key of Object.keys(session) as Array<keyof ObserveSessionMeta>) {
    if (session[key] === undefined) delete session[key];
  }
  return session;
}

function mergeObserveFiles(
  left: ObserveFile[] | undefined,
  right: ObserveFile[] | undefined,
): ObserveFile[] {
  const byPath = new Map<string, ObserveFile>();
  for (const file of [...left ?? [], ...right ?? []]) {
    const path = file.path?.trim();
    if (!path) continue;
    const existing = byPath.get(path);
    if (!existing) {
      byPath.set(path, { ...file, path });
      continue;
    }
    existing.touches += file.touches;
    existing.lastT = Math.max(existing.lastT, file.lastT);
    if (existing.state === "read" && file.state !== "read") existing.state = file.state;
    if (existing.state === "modified" && file.state === "created") existing.state = "created";
  }
  return [...byPath.values()].sort((leftFile, rightFile) => {
    const leftChanged = leftFile.state === "read" ? 0 : 1;
    const rightChanged = rightFile.state === "read" ? 0 : 1;
    if (leftChanged !== rightChanged) return rightChanged - leftChanged;
    return rightFile.lastT - leftFile.lastT;
  });
}

function turnFactsFromTailEvents(events: TailEvent[]): LaneFacts["turn"] {
  let startedAt = -1;
  let completedAt = -1;
  let startedCount = 0;
  for (const event of events) {
    const summary = event.summary.trim().toLowerCase();
    const type = payloadType(event);
    if (summary === "task started" || type === "task_started") {
      startedAt = Math.max(startedAt, event.ts);
      startedCount += 1;
    }
    if (summary === "task complete" || type === "task_complete" || type === "turn_aborted") {
      completedAt = Math.max(completedAt, event.ts);
    }
  }
  if (startedCount === 0 && completedAt < 0) return { phase: "idle" };
  return {
    phase: startedAt > completedAt ? "started" : "complete",
    index: startedCount || undefined,
  };
}

function currentTaskFromTailEvents(
  events: TailEvent[],
  observe: ObserveData | null | undefined,
): string | undefined {
  const latestObserveAsk = [...observe?.events ?? []]
    .reverse()
    .find((event) => event.kind === "ask" && event.text.trim());
  if (latestObserveAsk) return laneSnippetText(latestObserveAsk.text, 160, 2);

  for (const event of [...events].sort((left, right) => right.ts - left.ts)) {
    if (event.kind === "user" && event.summary.trim()) {
      return laneSnippetText(event.summary, 160, 2);
    }
    const payload = tailPayload(event);
    if (payloadType(event) === "user_message") {
      const message = stringValue(payload?.message);
      if (message) return laneSnippetText(message, 160, 2);
    }
  }
  return undefined;
}

function attributionFromEvents(events: TailEvent[]): TailHarness | undefined {
  let best: TailHarness | undefined;
  for (const event of events) {
    if (!best || (ATTRIBUTION_RANK[event.harness] ?? 0) > (ATTRIBUTION_RANK[best] ?? 0)) {
      best = event.harness;
    }
  }
  return best;
}

