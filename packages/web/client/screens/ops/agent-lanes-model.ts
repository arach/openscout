export type AgentLaneHorizonKey = "5m" | "30m" | "4h" | "24h";

export const DEFAULT_AGENT_LANE_HORIZON: AgentLaneHorizonKey = "5m";

export const AGENT_LANE_HORIZON_OPTIONS: ReadonlyArray<{
  key: AgentLaneHorizonKey;
  label: string;
  windowMs: number;
}> = [
  { key: "5m", label: "5m", windowMs: 5 * 60_000 },
  { key: "30m", label: "30m", windowMs: 30 * 60_000 },
  { key: "4h", label: "4h", windowMs: 4 * 60 * 60_000 },
  { key: "24h", label: "24h", windowMs: 24 * 60 * 60_000 },
];

export function agentLaneHorizonWindowMs(horizon: AgentLaneHorizonKey): number {
  return AGENT_LANE_HORIZON_OPTIONS.find((option) => option.key === horizon)?.windowMs
    ?? AGENT_LANE_HORIZON_OPTIONS[0].windowMs;
}

export function agentLaneHorizonLabel(horizon: AgentLaneHorizonKey): string {
  return AGENT_LANE_HORIZON_OPTIONS.find((option) => option.key === horizon)?.label
    ?? horizon;
}

/** Scale tail replay depth with the selected lane horizon. */
export function agentLaneTailRecentLimit(horizon: AgentLaneHorizonKey): number {
  switch (horizon) {
    case "5m":
      return 500;
    case "30m":
      return 2_000;
    case "4h":
      return 5_000;
    case "24h":
      return 10_000;
    default:
      return 500;
  }
}

