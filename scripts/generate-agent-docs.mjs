import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

const SOURCES = [
  "AGENTS.md",
  "README.md",
  "install.md",
  "docs/README.md",
  "docs/quickstart.md",
  "docs/current-posture.md",
  "docs/architecture.md",
  "docs/data-ownership.md",
  "docs/agent-integration-contract.md",
  "docs/agent-identity.md",
  "docs/collaboration-workflows-v1.md",
  "docs/operator-attention-and-unblock.md",
  "docs/scout-agent-delegation.md",
  "docs/agent/README.agent.md",
  "docs/agent/current-posture.agent.md",
  "docs/agent/integration-contract.agent.md",
];

function readSource(path) {
  return readFileSync(join(ROOT, path), "utf8").trim();
}

const body = [
  "# OpenScout Full LLM Context",
  "",
  "Generated by `bun scripts/generate-agent-docs.mjs`.",
  "",
  "Current posture: OpenScout is for high-trust local developer pilots. It is not enterprise-ready, compliance-ready, a hardened multi-tenant runtime, or a guaranteed distributed delivery layer. License posture is not finalized; check package metadata and repo license files before making reuse claims.",
  "",
  "## Included Files",
  "",
  ...SOURCES.map((source) => `- \`${source}\``),
  "",
  ...SOURCES.flatMap((source) => [
    "---",
    "",
    `## ${source}`,
    "",
    `<!-- BEGIN ${source} -->`,
    "",
    readSource(source),
    "",
    `<!-- END ${source} -->`,
    "",
  ]),
].join("\n").trimEnd();

writeFileSync(join(ROOT, "llms-full.txt"), `${body}\n`);
writeFileSync(join(ROOT, "landing/public/llms-full.txt"), `${body}\n`);

console.log(`Wrote llms-full.txt from ${SOURCES.length} source files.`);
